async function main() {
  const matchedBarcodes: string[] = [];
  const notMatchedBarcodes: string[] = [];

  for (const code of barcodes) {
    const hit = await prisma.inventory.findUnique({
      where: {
        barcode: code,
      },
      select: { barcode: true },
    });

    if (hit) {
      matchedBarcodes.push(hit.barcode);
      const logMsg = `âœ… ${code} â†’ match #${matchedBarcodes.length}`;
      console.log(logMsg);
      appendFileSync(logFilePath, logMsg + "\n");
    } else {
      notMatchedBarcodes.push(code);
      console.log(`âŒ ${code} â†’ no match`);
    }
  }

  writeFileSync(
    "./inventory/existing_barcodes.json",
    JSON.stringify(matchedBarcodes, null, 2)
  );
  writeFileSync(
    "./inventory/non_existing_barcodes.json",
    JSON.stringify(notMatchedBarcodes, null, 2)
  );
  console.log(
    `ðŸ“„ existing_barcodes.json written with ${matchedBarcodes.length} matches, not matching barcodes: ${notMatchedBarcodes.length}`
  );
}

main()
  .catch((err) => {
    console.error("âŒ Error:", err);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
