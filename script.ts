  const results = await prisma.customer.findMany({
    where: {
      email: {
        in: emails,
        mode: 'insensitive', // Optional â€” remove if you want case-sensitive match
      },
    },
    select: {
      email: true,
    },
  })

  const matchedBarcodes = results.map((item) => item.email)

  console.log(`âœ… Found ${matchedBarcodes.length} exact matches.`)

